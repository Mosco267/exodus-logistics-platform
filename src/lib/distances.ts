// ─── Haversine formula ───────────────────────────────────────
export function haversine(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371; // Earth radius in km
  const toRad = (deg: number) => deg * Math.PI / 180;
  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a = Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function getCountryDistance(fromCode: string, toCode: string): number {
  const from = COUNTRY_COORDS[fromCode];
  const to = COUNTRY_COORDS[toCode];
  if (!from || !to) return 5000; // sensible default
  return haversine(from.lat, from.lng, to.lat, to.lng);
}

export function getStateDistance(countryCode: string, fromState: string, toState: string): number {
  const states = STATE_COORDS[countryCode];
  if (!states) return 300; // default for countries without state mapping
  const from = states[fromState];
  const to = states[toState];
  if (!from || !to) return 300;
  return haversine(from.lat, from.lng, to.lat, to.lng);
}